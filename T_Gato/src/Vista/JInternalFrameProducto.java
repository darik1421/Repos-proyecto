    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
      String cod_produc=jTextCodigo.getText();
      String descripcion=jTextDescripcion.getText();
      String precio=jTextPrecio.getText();
      String unid_med=jTextUnidMed.getText();
      String existencia=jTextExistencia.getText();
      String t_produc=jTextTProducto.getText();
      
      if(cod_produc.contentEquals("")|| descripcion.contentEquals("")|| precio.contentEquals("")||
              unid_med.contentEquals("")|| existencia.contentEquals("")|| t_produc.contentEquals("")){
          JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
      }
      else{
          try{
              float p=Float.parseFloat(precio);
              float um=Float.parseFloat(unid_med);
              float ex=Float.parseFloat(existencia);
              
             Producto produc=new DAOProducto().Insertar(descripcion,p,um,ex, t_produc);
              JOptionPane.showMessageDialog(rootPane, "Registro agregado");
          }
          catch(Exception e){
              e.printStackTrace();
              JOptionPane.showMessageDialog(rootPane, "No agregó el registro");
          }
      }
      obtenerDatos();
      
      limpiarCampos();
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int fila=this.jTableProducto.getSelectedRow();
        if(fila==-1){
            JOptionPane.showConfirmDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            try{
                int cod_produc=Integer.parseInt((String)this.jTableProducto.getValueAt(fila, 0).toString());
                String descripcion=(String)this.jTableProducto.getValueAt(fila, 1);
                float precio=Float.parseFloat((String)this.jTableProducto.getValueAt(fila, 2).toString());
                float unid_med=Float.parseFloat((String)this.jTableProducto.getValueAt(fila, 3).toString());
                float existencia=Float.parseFloat((String)this.jTableProducto.getValueAt(fila, 4).toString());
                String t_produc=(String)this.jTableProducto.getValueAt(fila, 5);
                
                jTextCodigo.setText(""+cod_produc);
                jTextDescripcion.setText(descripcion);
                jTextPrecio.setText(""+precio);
                jTextUnidMed.setText(""+unid_med);
                jTextExistencia.setText(""+existencia);
                jTextTProducto.setText(t_produc);
                
            }
            catch(NumberFormatException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

