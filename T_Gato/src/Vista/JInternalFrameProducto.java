    public void obtenerDatos(){
        List<Producto> productos = new DAOProducto().obtenerDatos();
        DefaultTableModel modelo = new DefaultTableModel();
        
        String[] columnas={"cod_produc","descripcion","existencia","precio","unid_med","t_produc"};
        
        modelo.setColumnIdentifiers(columnas);
        
        for(Producto produc:productos){
            
            String[]renglon={Integer.toString(produc.getCod_produc()),produc.getDescripcion(),Float.toString(produc.getExistencia()),
               Float.toString(produc.getPrecio()),produc.getT_produc(),Float.toString(produc.getUnid_med())};
            modelo.addRow(renglon);
        }
        jTableProducto.setModel(modelo);
    }
    
     public void actualizarProducto(){
        int cod_produc=Integer.parseInt(this.jTextCodigo.getText());
        String descripcion=this.jTextDescripcion.getText();
        float existencia=Float.parseFloat(this.jTextExistencia.getText());
        float precio=Float.parseFloat(this.jTextPrecio.getText());
        float unid_med=Float.parseFloat(this.jTextUnidMed.getText());
        String t_produc=this.jTextTProducto.getText();
        
        
         DAOProducto dao=new DAOProducto();
       int res=dao.Actualizar(cod_produc, descripcion, precio, unid_med, existencia, t_produc);
       if(res==1){
           JOptionPane.showMessageDialog(rootPane, "¡Producto Actualizado!");
       }
       else{
           JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
       }
     }
