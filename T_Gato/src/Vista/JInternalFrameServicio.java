    public void obtenerDatos(){
        List<Servicio> servicios=new DAOServicio().obtenerDatos();
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[] columnas={"cod_serv", "descripcion", "fecha_serv", "precio", "id_empleado"};
        
        modelo.setColumnIdentifiers(columnas);
        
        for(Servicio serv:servicios){
            String[] renglon={Integer.toString(serv.getCod_serv()), serv.getDescripcion(),Float.toString(serv.getPrecio()),
            Integer.toString(serv.getId_empleado()),serv.getFecha_serv().toString()};
            
            modelo.addRow(renglon);
        }
        jTableServicio.setModel(modelo);
        
        }
    
     public void actualizarServicio(){
        int cod_serv=Integer.parseInt(this.jTextCodigo.getText());
        String descripcion=this.jTextDescripcion.getText();
        float precio=Float.parseFloat(this.jTextPrecio.getText());
        Date fecha_serv=Date.valueOf(this.jTextFecha.getText());
        int id_empleado=Integer.parseInt(this.jTextIdEmpl.getText());
        
         DAOServicio dao=new DAOServicio();
       int res=dao.Actualizar(cod_serv, descripcion, fecha_serv, precio, id_empleado);
       if(res==1){
           JOptionPane.showMessageDialog(rootPane, "¡Servicio Actualizado!");
       }
       else{
           JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
       }
    }
