    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        String descripcion=jTextDescripcion.getText();
        String precio=jTextPrecio.getText();
        String fecha_serv=jTextFecha.getText();
        String id_empleado=jTextIdEmpl.getText();
        
        if(descripcion.contentEquals("")|| precio.contentEquals("")|| fecha_serv.contentEquals("")|| id_empleado.contentEquals("")){
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
        }
        else{
            try{
                float pre=Float.parseFloat(precio);
                Date fech=Date.valueOf(fecha_serv);
                int id_empl=Integer.parseInt(id_empleado);
                Servicio serv=new DAOServicio().Insertar(descripcion, fech, pre, id_empl);
                 JOptionPane.showMessageDialog(rootPane, "Registro agregado");
            }
            catch(Exception e){
               e.printStackTrace();
               JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
           }
                
        }
        obtenerDatos();
        limpiarCampos();
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
         int fila=jTableServicio.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            try{
                int cod_serv=Integer.parseInt((String)this.jTableServicio.getValueAt(fila, 0));
                String descripcion=(String)this.jTableServicio.getValueAt(fila, 1);
                Date fecha_serv=Date.valueOf((String)this.jTableServicio.getValueAt(fila, 2).toString());
                String id_empleado=(String)this.jTableServicio.getValueAt(fila, 3);
                String precio=(String)this.jTableServicio.getValueAt(fila, 4);

                jTextCodigo.setText(""+cod_serv);
                jTextDescripcion.setText(descripcion);
                jTextFecha.setText(String.valueOf(fecha_serv));
                jTextIdEmpl.setText(id_empleado);
                jTextPrecio.setText(precio);
            }
            catch (NumberFormatException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

