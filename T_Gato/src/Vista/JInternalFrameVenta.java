    public void obtenerDatos(){
        List<Venta> ventas=new DAOVenta().obtenerDatos();
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[] columnas={"n_factura", "descripcion", "fecha_vent", "id_cliente", "id_empleado"};
        
        modelo.setColumnIdentifiers(columnas);
        
        for(Venta vent:ventas){
            String[] renglon={Integer.toString(vent.getN_factura()), vent.getDescripcion(), vent.getFecha_vent().toString(), 
                Integer.toString(vent.getId_empleado()),Integer.toString(vent.getId_cliente())};
                
            modelo.addRow(renglon);
            }
        jTableVenta.setModel(modelo);
    }
     
    public void actualizarVenta(){
        int n_factura=Integer.parseInt(this.jTextNFactura.getText());
        String descripcion=this.jTextDescripcion.getText();
        Date fecha_vent=Date.valueOf(this.jTextFecha.getText());
        int id_empleado=Integer.parseInt(this.jTextIdEmpl.getText());
        int id_cliente=Integer.parseInt(this.jTextIdCliente.getText());
        
        DAOVenta dao=new DAOVenta();
        int res=dao.Actualizar(n_factura, fecha_vent, descripcion, id_empleado, id_cliente);
        if(res==1){
            JOptionPane.showMessageDialog(rootPane, "¡Venta Actualizada!");
        }
        else{
           JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
       }
    }
         

