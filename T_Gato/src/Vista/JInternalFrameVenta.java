    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
         String descripcion=jTextDescripcion.getText();
         String fecha_vent=jTextFecha.getText();
         String id_empleado=jTextIdEmpl.getText();
         String id_cliente=jTextIdCliente.getText();
         
         if(descripcion.contentEquals("")|| fecha_vent.contentEquals("")|| id_empleado.contentEquals("")||
                 id_cliente.contentEquals("")){
             JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios");
         }
         else{
             try{
                 Date fech=Date.valueOf(fecha_vent);
                 int id_empl=Integer.parseInt(id_empleado);
                 int id_cli=Integer.parseInt(id_cliente);
                 Venta vent=new DAOVenta().Insertar(fech, descripcion, id_empl, id_cli);
                 JOptionPane.showMessageDialog(rootPane, "Registro agregado");
             }
             catch(Exception e){
                 e.printStackTrace();
                 JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
             }
         }
         obtenerDatos();
         limpiarCampos();
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int fila=jTableVenta.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
        }
        else{
            try{
                int n_factura=Integer.parseInt((String)this.jTableVenta.getValueAt(fila, 0));
                String descripcion=(String)this.jTableVenta.getValueAt(fila, 1);
                Date fecha_vent=Date.valueOf((String)this.jTableVenta.getValueAt(fila, 2).toString());
                String id_empleado=(String)this.jTableVenta.getValueAt(fila, 3);
                String id_cliente=(String)this.jTableVenta.getValueAt(fila, 4);

                jTextNFactura.setText(""+n_factura);
                jTextDescripcion.setText(descripcion);
                jTextFecha.setText(String.valueOf(fecha_vent));
                jTextIdEmpl.setText(id_empleado);
                jTextIdCliente.setText(id_cliente);
            }
            catch (NumberFormatException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

