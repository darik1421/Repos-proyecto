    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed

       String cedula=jTextCedula.getText();
       String nombres=jTextNombres.getText();
       String apellidos=jTextApellidos.getText();
       String fecha_nac=jTextFechaNac.getText();
       String direccion=jTextDireccion.getText();
       String sexo=jTextSexo.getText();
       String celular=jTextCelular.getText();
       
       if(cedula.contentEquals("")|| nombres.contentEquals("")|| apellidos.contentEquals("")||
               fecha_nac.contentEquals("")|| direccion.contentEquals("")|| sexo.contentEquals("")|| celular.contentEquals("")){
           JOptionPane.showInternalMessageDialog(rootPane, "Todos los campos son obligatorios");
       }
       else{
           try{
               Date fech=Date.valueOf(fecha_nac);
               char sex=this.jTextSexo.getText().charAt(0);
               Empleado empl=new DAOEmpleado().Insertar(cedula, nombres, apellidos, fech, direccion, sex, celular);
               JOptionPane.showMessageDialog(rootPane, "Registro agregado");
           }
           catch(Exception e){
               e.printStackTrace();
               JOptionPane.showMessageDialog(rootPane, "No se agregó el registro");
           }
       }
       obtenerDatos();
       limpiarCampos();
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
       int fila=this.jTableEmpleado.getSelectedRow();
       if(fila==-1){
           JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla");
       }
       else{
           try{
               int id_empl=Integer.parseInt((String)this.jTableEmpleado.getValueAt(fila, 0).toString());
               String cedula=(String)this.jTableEmpleado.getValueAt(fila, 1);
               String nombres=(String)this.jTableEmpleado.getValueAt(fila, 2);
               String apellidos=(String)this.jTableEmpleado.getValueAt(fila, 3);
               Date fecha_nac=Date.valueOf((String)this.jTableEmpleado.getValueAt(fila, 4).toString());
               String direccion=(String)this.jTableEmpleado.getValueAt(fila, 5);
               String sexo=(String)this.jTableEmpleado.getValueAt(fila, 6);
               String celular=(String)this.jTableEmpleado.getValueAt(fila, 7);
               
               jTextIdEmpleado.setText(""+id_empl);
               jTextCedula.setText(cedula);
               jTextNombres.setText(nombres);
               jTextApellidos.setText(apellidos);
               jTextFechaNac.setText(String.valueOf(fecha_nac));
               jTextDireccion.setText(direccion);
               jTextSexo.setText(sexo);
               jTextCelular.setText(celular);    
           }
           catch(NumberFormatException e){
               e.printStackTrace();
               
           }
       }
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

