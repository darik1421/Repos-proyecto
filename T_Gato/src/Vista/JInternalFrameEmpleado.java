    public void obtenerDatos(){
        List<Empleado> empleados = new DAOEmpleado().obtenerDatos();
        DefaultTableModel modelo = new DefaultTableModel();
        
        String[] columnas={"id_empl","cedula","nombres","apellidos","fecha_nac","direccion","sexo","celular"};
        
        modelo.setColumnIdentifiers(columnas);
        
        for(Empleado empl:empleados){
            
            String[]renglon={Integer.toString(empl.getId_empl()),empl.getCedula(),empl.getNombres(),
                empl.getApellidos(),empl.getDireccion(),Character.toString(empl.getSexo()),empl.getCelular(),empl.getFecha_nac().toString()};
            modelo.addRow(renglon);
        }
        jTableEmpleado.setModel(modelo);
    }
    
    public void actualizarEmpleado(){
        int id=Integer.parseInt(this.jTextIdEmpleado.getText());
        String cedula=this.jTextCedula.getText();
        String nombres=this.jTextNombres.getText();
        String apellidos=this.jTextApellidos.getText();
        String direccion=this.jTextDireccion.getText();
        char sexo=this.jTextSexo.getText().charAt(0);
        String celular=this.jTextCelular.getText();
        Date fecha_nac=Date.valueOf(this.jTextFechaNac.getText());
        
         DAOEmpleado dao=new DAOEmpleado();
       int res=dao.Actualizar(id, cedula, nombres, apellidos, fecha_nac, direccion, sexo, celular);
       if(res==1){
           JOptionPane.showMessageDialog(rootPane, "¡Empleado Actualizado!");
       }
       else{
           JOptionPane.showMessageDialog(rootPane, "¡Ocurrió un ERROR!");
       }
    }
    
    

